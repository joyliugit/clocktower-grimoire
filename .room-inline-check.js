        function bytesToBase64Url(bytes) {
            let binary = '';
            const chunkSize = 0x8000;
            for (let index = 0; index < bytes.length; index += chunkSize) {
                binary += String.fromCharCode(...bytes.subarray(index, index + chunkSize));
            }
            return btoa(binary).replace(/\+/g, '-').replace(/\//g, '_').replace(/=+$/g, '');
        }

        function base64UrlToBytes(value) {
            let base64 = `${value || ''}`.replace(/-/g, '+').replace(/_/g, '/');
            while (base64.length % 4) base64 += '=';
            const binary = atob(base64);
            const bytes = new Uint8Array(binary.length);
            for (let index = 0; index < binary.length; index++) bytes[index] = binary.charCodeAt(index);
            return bytes;
        }

        async function deriveOfflineSeatKey(roomCode, seatIndex, pin) {
            if (!window.crypto?.subtle) throw new Error('当前浏览器不支持离线加密，请换到系统浏览器再试。');
            const material = roomEncoder.encode(`clocktower|${roomCode}|${seatIndex}|${pin}`);
            const digest = await crypto.subtle.digest('SHA-256', material);
            return crypto.subtle.importKey('raw', digest, { name: 'AES-GCM' }, false, ['encrypt', 'decrypt']);
        }

        async function encryptOfflinePayload(roomCode, seatIndex, pin, payload) {
            const key = await deriveOfflineSeatKey(roomCode, seatIndex, pin);
            const iv = crypto.getRandomValues(new Uint8Array(12));
            const plain = roomEncoder.encode(JSON.stringify(payload));
            const cipher = new Uint8Array(await crypto.subtle.encrypt({ name: 'AES-GCM', iv }, key, plain));
            return { i: bytesToBase64Url(iv), d: bytesToBase64Url(cipher) };
        }

        async function decryptOfflinePayload(roomCode, seatIndex, pin, packet) {
            try {
                const key = await deriveOfflineSeatKey(roomCode, seatIndex, pin);
                const iv = base64UrlToBytes(packet.i);
                const cipher = base64UrlToBytes(packet.d);
                const plain = await crypto.subtle.decrypt({ name: 'AES-GCM', iv }, key, cipher);
                return JSON.parse(roomDecoder.decode(new Uint8Array(plain)));
            } catch (error) {
                return null;
            }
        }

