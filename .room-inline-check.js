        function ensureOfflineSeatPins(seatCount = getOfflineSeatCount()) {
            const nextPins = { ...(roomSession.seatPins || {}) };
            let changed = false;
            for (let index = 0; index < seatCount; index++) {
                const currentValue = sanitizeSeatPin(nextPins[index]);
                if (currentValue.length !== 4) {
                    nextPins[index] = createSeatPin();
                    changed = true;
                } else {
                    nextPins[index] = currentValue;
                }
            }
            Object.keys(nextPins).forEach(key => {
                if (Number(key) >= seatCount) {
                    delete nextPins[key];
                    changed = true;
                }
            });
            if (changed) roomSession.seatPins = nextPins;
            return nextPins;
        }

