        renderCircle = function renderCircleRoomAware() {
            if (appMode !== 'player') {
                const stage = document.getElementById('playerCircleStage');
                const playerWheel = document.getElementById('playerCircleView');
                const playerEmpty = document.getElementById('playerCircleEmpty');
                if (playerWheel) playerWheel.style.display = 'none';
                if (playerEmpty) playerEmpty.style.display = 'none';
                return originalRenderCircle();
            }

            const stage = document.getElementById('playerCircleStage');
            const container = document.getElementById('playerCircleView');
            const empty = document.getElementById('playerCircleEmpty');
            if (!stage || !container || !empty) return;

            const seatCount = Number(roomSession.seatCount) || (roomSession.role === 'player' ? getSelectableSeatCount() : 0);
            if (!roomSession.roomCode || seatCount <= 0) {
                container.style.display = 'none';
                empty.style.display = 'block';
                return;
            }

            empty.style.display = 'none';
            container.style.display = 'block';
            container.innerHTML = '';

            const stageStyle = window.getComputedStyle(stage);
            const horizontalPadding = parseFloat(stageStyle.paddingLeft) + parseFloat(stageStyle.paddingRight);
            const availableWidth = Math.max(250, stage.clientWidth - horizontalPadding);
            const size = availableWidth < 300 ? availableWidth : Math.min(availableWidth, 420);
            const seatSize = Math.min(seatCount >= 12 ? 56 : seatCount >= 9 ? 60 : 66, Math.floor(size * 0.21));
            container.style.setProperty('--altar-size', `${size}px`);
            container.style.setProperty('--seat-size', `${seatSize}px`);
            container.style.setProperty('--seat-name-size', `${Math.max(11, Math.round(seatSize * 0.22))}px`);
            container.style.setProperty('--seat-role-size', `${Math.max(9, Math.round(seatSize * 0.17))}px`);

            const center = size / 2;
            const radius = center - seatSize / 2 - 18;
            const angleStep = (2 * Math.PI) / seatCount;
            for (let index = 0; index < seatCount; index++) {
                const angle = index * angleStep - Math.PI / 2;
                const x = center + radius * Math.cos(angle);
                const y = center + radius * Math.sin(angle);
                const noteRadius = radius + seatSize * 1.02;
                const noteX = center + noteRadius * Math.cos(angle);
                const noteY = center + noteRadius * Math.sin(angle);
                const note = getPlayerSeatNote(index);
                const seat = document.createElement('div');
                seat.className = 'seat';
                seat.style.cssText = `left:${x}px; top:${y}px;`;
                seat.title = `点击记录你对座位 ${index + 1} 的猜测`;
                seat.onclick = () => openPlayerGuessModal(index);
                seat.innerHTML = `<div class="seat-name">${escapeHtml(getPlayerSeatDisplayName(index))}</div>`;

                const guessNote = document.createElement('div');
                guessNote.className = 'seat-ability player-guess-note';
                guessNote.style.cssText = `left:${noteX}px; top:${noteY}px;`;
                guessNote.innerText = note.guessedRole ? getPlayerGuessRoleLabel(note.guessedRole) : '点击标记';

                container.appendChild(guessNote);
                container.appendChild(seat);
            }
        };

