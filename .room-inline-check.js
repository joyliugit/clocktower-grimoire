        function getRoleIcon(roleStr, player = null) {
            if (isTravelerPlayer(player)) return travelerRoleIconMap[getTravelerName(player)] || '🧳';
            const normalizedRole = normalizeRoleName(roleStr || player?.role);
            if (!normalizedRole || normalizedRole === '❓ 未分配') return '❓';
            if (normalizedRole === TRAVELER_ROLE || `${normalizedRole}`.startsWith('旅行者：')) {
                const travelerName = player ? getTravelerName(player) : `${normalizedRole}`.split('：')[1];
                return travelerRoleIconMap[travelerName] || '🧳';
            }
            if (roleIconMap[normalizedRole]) return roleIconMap[normalizedRole];
            const category = getRoleCategory(normalizedRole);
            if (category === 'Townsfolk') return '✦';
            if (category === 'Outsider') return '◇';
            if (category === 'Minion') return '◆';
            if (category === 'Demon') return '✹';
            if (category === 'Traveler') return '🧳';
            return '❓';
        }
