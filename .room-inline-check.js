        function loadData() {
            const saved = localStorage.getItem(STORAGE_KEY) || LEGACY_STORAGE_KEYS.map(key => localStorage.getItem(key)).find(Boolean);
            if (saved) {
                const data = JSON.parse(saved);
                selectedScript = scriptMeta[data.selectedScript] ? data.selectedScript : 'tb';
                players = (data.players || []).map((player, index) => normalizePlayerData(player, index));
                bluffs = (data.bluffs || ['❓ 未分配', '❓ 未分配', '❓ 未分配']).map(normalizeRoleName);
                currentNightOrderType = data.currentNightOrderType || '';
                playerListLayout = data.playerListLayout || (window.innerWidth >= 720 ? 'double' : 'single');
                scoreBoard = normalizeScoreBoardData(data.scoreBoard || {});
                seriesGames = typeof data.seriesGames === 'number' ? data.seriesGames : 0;
                finalMvp = data.finalMvp || '';
                travelerFeatureEnabled = typeof data.travelerFeatureEnabled === 'boolean'
                    ? data.travelerFeatureEnabled
                    : players.some(player => isTravelerPlayer(player));
                configuredPlayerCount = typeof data.configuredPlayerCount === 'number'
                    ? data.configuredPlayerCount
                    : getCurrentPlayerRange().min;
                appMode = data.appMode === 'player' ? 'player' : 'storyteller';
                roomSession = {
                    ...createEmptyRoomSession(),
                    ...(data.roomSession || {})
                };
                roomSession.roomCode = sanitizeRoomCode(roomSession.roomCode || '');
                roomSession.seatPin = sanitizeSeatPin(roomSession.seatPin || '');
                roomSession.connected = !!roomSession.roomCode;
                roomSession.seatIndex = Number.isInteger(roomSession.seatIndex) ? roomSession.seatIndex : null;
                roomSession.playerSeatNotes = normalizePlayerSeatNotes(roomSession.playerSeatNotes || {});
                playerInboxCard = data.playerInboxCard || null;
                roomSummary = null;
                ensureOfflineSeatPins(roomSession.seatCount || players.length || getConfiguredSeatCount() || 0);
                if (players.length > 0) document.getElementById('timerPanel').style.display = 'block';
            } else {
                playerListLayout = window.innerWidth >= 720 ? 'double' : 'single';
                selectedScript = 'tb';
                travelerFeatureEnabled = false;
                configuredPlayerCount = getCurrentPlayerRange().min;
                appMode = 'storyteller';
                roomSession = createEmptyRoomSession();
                roomSummary = null;
                playerInboxCard = null;
            }
            initBluffSelects();
            initRoleRevealSelect();
            updateScriptUi();
            renderAll();
            updateTimerDisplay();
        }

