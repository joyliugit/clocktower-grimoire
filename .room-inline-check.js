        function renderNightOrderColumnHtml(type) {
            const activeRoles = players.map(player => normalizeRoleName(player.role));
            const activeNightOrder = getCurrentNightOrder();
            const activeRoleDetails = getCurrentRoleDetails();
            const hideTbEvilIntroSteps = shouldHideTbEvilIntroSteps();
            let stepCount = 1;
            let itemsHtml = '';

            activeNightOrder[type].forEach(role => {
                if (hideTbEvilIntroSteps && (role === '【系统：爪牙互相确认】' || role === '【系统：恶魔确认爪牙及3个伪装】')) return;
                const details = activeRoleDetails[role] || { ability: '', action: '无详细操作说明' };
                if (role.startsWith('【')) {
                    itemsHtml += `<li class="night-item"><div class="night-header">${stepCount}. <span class="system-prompt">${role}</span></div><div class="storyteller-action">👉 <b>说书人：</b>${details.action}</div></li>`;
                    stepCount++;
                    return;
                }
                if (!activeRoles.includes(role)) return;
                players.filter(player => normalizeRoleName(player.role) === role).forEach(player => {
                    const status = player.isAlive ? '' : ' <span class="status-dead">(已死亡)</span>';
                    const shortRole = role.split('：')[1] || role;
                    const aiHtml = getAIRecommendation(role, player);
                    itemsHtml += `
                        <li class="night-item">
                            <div class="night-header">${stepCount}. 唤醒 <span class="highlight">${shortRole}</span> [${player.name}] ${status}</div>
                            <div class="role-ability">${details.ability}</div>
                            <div class="storyteller-action">👉 <b>说书人：</b>${details.action}</div>
                            ${aiHtml}
                        </li>
                    `;
                    stepCount++;
                });
            });

            return `
                <section class="night-order-column">
                    <div class="night-order-column-header">${type === 'first' ? '🌙 首夜' : '🌘 其余夜晚'}</div>
                    <ul class="night-list">${itemsHtml || '<li class="night-item"><div class="role-ability">当前没有需要处理的角色。</div></li>'}</ul>
                </section>
            `;
        }

